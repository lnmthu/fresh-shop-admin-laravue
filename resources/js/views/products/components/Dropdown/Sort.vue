<template>
  <el-dropdown :show-timeout="100" trigger="click">
    <el-button plain>
      {{ sort===1 ? 'Priority' : 'No Priority' }}
      <i class="el-icon-caret-bottom el-icon--right" />
    </el-button>
    <el-dropdown-menu slot="dropdown" class="no-padding">
      <el-dropdown-item>
        <el-radio-group v-model="sort" style="padding: 10px;">
          <el-radio :label="1">
            Priority
          </el-radio>
          <el-radio :label="2">
            No Priority
          </el-radio>
        </el-radio-group>
      </el-dropdown-item>
    </el-dropdown-menu>
  </el-dropdown>
</template>

<script>
export default {
  props: {
    value: {
      type: Number,
      default: 1,
    },
  },
  computed: {
    sort: {
      get() {
        return this.value;
      },
      set(val) {
        this.$emit('input', val);
      },
    },
  },
};
</script>
